// Создаем пустой массив
const matrix = [];

// Внешний цикл: создаем строки (подмассивы)
for (let i = 0; i < 3; i++) {
    const row = []; // Создаем пустой подмассив для каждой строки

    // Внутренний цикл: заполняем строку значениями 1
    for (let j = 0; j < 3; j++) {
        row.push(1); // Добавляем значение 1 в подмассив
    }

    matrix.push(row); // Добавляем готовый подмассив во внешний массив
}

console.log(matrix); // Выводим результат в консоль

